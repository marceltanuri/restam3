package io.github.marceltanuri.frameworks.restam3.http;

/**
 * Represents an HTTP method.
 *
 * @author Marcel Tanuri
 */
public enum HttpMethod {
        /**
     * GET method.
     */
    GET,
    /**
     * POST method.
     */
    POST,
    /**
     * PUT method.
     */
    PUT,
    /**
     * PATCH method.
     */
    PATCH,
    /**
     * DELETE method.
     */
    DELETE,
    /**
     * HEAD method.
     */
    HEAD,
    /**
     * OPTIONS method.
     */
    OPTIONS,
    /**
     * TRACE method.
     */
    TRACE,
    /**
     * CONNECT method.
     */
    CONNECT
}
